import { Controller } from "@hotwired/stimulus"

// Connects to data-controller="map"
export default class extends Controller {

  static values = {
    apiKey: String,
    markers: Array
  }

  connect() {
    mapboxgl.accessToken = this.apiKeyValue

    this.map = new mapboxgl.Map({
      container: this.element,
      style: "mapbox://styles/jane-doronina/cl7ropavj000t15lckgo1ky9j"
    })

    this.#addMarkersToMap()
    this.#fitMapToMarkers()
  }

  #addMarkersToMap() {
    console.log(this.markersValue);
    this.markersValue.forEach((marker) => {
      const popup = new mapboxgl.Popup().setHTML(marker.people_window)

      const customMarker = document.createElement("div")
      customMarker.className = "marker"

      customMarker.insertAdjacentHTML('afterbegin', marker.marker)

      new mapboxgl.Marker(customMarker)
        .setLngLat([ marker.lng, marker.lat ])
        .setPopup(popup)
        .addTo(this.map)
    })
  }

  #fitMapToMarkers() {
    const bounds = new mapboxgl.LngLatBounds()
    this.markersValue.forEach(marker => {
      if(marker.people_window.includes('<li class="person">')) {
        bounds.extend([ marker.lng, marker.lat ]);
      }
    });
    this.map.fitBounds(bounds, { padding: 0, maxZoom: 15, duration: 0 })
  }
}
